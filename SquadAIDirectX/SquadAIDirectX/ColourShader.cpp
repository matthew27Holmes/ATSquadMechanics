ColourShader::ColourShader(HINSTANCE hInstance): DXApp(hInstance)
{
	m_vertexShader = 0;
	m_pixelShader = 0;
	m_layout = 0;
	m_matrixBuffer = 0;
}


ColourShader::~ColourShader()
{
	Memory::SafeRelease(m_matrixBuffer);
	Memory::SafeRelease(m_layout);
	Memory::SafeRelease(m_pixelShader);
	Memory::SafeRelease(m_vertexShader);
}

bool ColourShader::Init(ID3D11Device* device)
{
	// Initialize the vertex and pixel shaders.
	bool result;

	result = InitializeShader(device,L"../SquadAIDirectX/colourVS.hlsl", L"../SquadAIDirectX/colourPS.hlsl");
	if (!result)
	{
		return false;
	}

	return true;
}


